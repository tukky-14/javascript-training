// 配列
const array = ['apple', 'banana', 'orange'];
// 配列の要素を取得
console.log(array[0]); // apple

// オブジェクト配列
const objArray = [
    { id: 1, name: 'taro' },
    { id: 2, name: 'jiro' },
    { id: 3, name: 'hanako' },
];
// オブジェクト配列の要素を取得
console.log(objArray[0].id); // 1
console.log(objArray[1].name); // jiro
