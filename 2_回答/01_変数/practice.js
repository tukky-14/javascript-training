// ### 問題1: 変数の宣言と代入
// JavaScriptで変数を宣言し、文字列「Hello, World!」を代入して、コンソールに表示させてください。

const hello = 'Hello, World!';
console.log(hello);

// ### 問題2: 算術演算子
// 2つの数値変数を宣言し、それらの和、差、積、商をコンソールに表示させてください。

const num1 = 10;
const num2 = 5;
console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num1 / num2);

// ### 問題3: 型の確認
// 異なる型（文字列、数値、真偽値）の変数をそれぞれ宣言し、それぞれの型をコンソールに表示させてください。※値ではなく、"型"を表示させます。

const str = 'Hello, World!';
const num = 10;
const bool = true;
console.log(typeof str);
console.log(typeof num);
console.log(typeof bool);
